// Incrementando de 1 a 10
for (let index = 1; index <= 10; index++) {
   console.log(index);
}


// Decrementando de 10 a 0
let counter = 10;

while(counter >= 1){
    console.log(counter);
    counter--;
}

// Números ímpares no intervalo
for(let i = 1; i <= 100; i += 2){
    console.log(i);
}

// Números pares no intervalo
for(let i = 0; i <= 100; i += 2){
    console.log(i);
}